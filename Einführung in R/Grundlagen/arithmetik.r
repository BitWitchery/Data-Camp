# Arithmetik mit R

# In seiner einfachsten Form kann R als simpler Taschenrechner verwendet werden. 
# Dafür gibt es die folgenden arithmetischen Operatoren:

#     Addition: +
#     Subtraktion: -
#     Multiplikation: *
#     Division: /
#     Potenzierung: ^
#     Modulo: %%

# Die letzten beiden sind vielleicht erklärungsbedürftig:

#     Der Operator ^ berechnet den Potenzwert aus der Basis auf seiner linken Seite und dem Exponenten 
#     auf seiner rechten Seite. Zum Beispiel ergibt 3^2 also den Wert 9.
#     Der Modulo-Operator gibt den Rest aus der Division der Zahl links durch die Zahl rechts zurück. 
#     Zum Beispiel ergibt 5 modulo 3 bzw. 5 %% 3 den Wert 2.

# Nutze dieses Wissen, um den Anweisungen zu folgen und die Übung abzuschließen.

############################################################################################################

