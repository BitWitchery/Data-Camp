# Was ist das für ein Datentyp?

# Wie zuvor erwähnt, würde die Addition von 5 + "six" zu einer Fehlermeldung wegen nicht 
# übereinstimmender Datentypen führen. Du kannst solche peinlichen Situationen vermeiden, 
# indem du den Datentyp einer Variablen vorher überprüfst. Das ist mit der Funktion class() möglich, 
# wie der Code im Editor zeigt.

##########################################################################################################

# Vervollständige den Code im Editor und gib auch die Klassen von my_character und my_logical aus.

# Declare variables of different types
my_numeric <- 42
my_character <- "universe"
my_logical <- FALSE 

# Check class of my_numeric
class(my_numeric)

# Check class of my_character
class(my_character)

# Check class of my_logical
class(my_logical)