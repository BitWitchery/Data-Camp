# Variablen ermöglichen es in Python, mit Namen auf bestimmte Werte zu verweisen. 
# Um eine Variable x mit dem Wert 5 zu deklarieren, wird = genutzt, wie in diesem Beispiel:

# x = 5

# Du kannst nun den Namen dieser Variablen (x) anstelle des tatsächlichen Wertes (5) verwenden.

# Denk daran, dass in Python mit = keine Gleichheitsprüfung durchgeführt wird, sondern eine Zuweisung erfolgt! 

